
/**
 * Creates an item with a unique name.
 * 
 * @author
 *
 */
public class Item
{
    private String name;
   
    public Item(String name)
    {
        this.name=name;
    }
   
    /**
     * Returns the name of the item.
     */
    public String getName()
    {
        return name;
    }
}
